class Product {
  String id;
  String title;
  String description;
  double price;
  double rating;
  String imageUrl;
  String brandName;
  String followers;
  String logo;

  Product ({
    required this.id,
    required this.title,
    required this.description,
    required this.price,
    required this.rating,
    required this.imageUrl,
    required this.brandName,
    required this.followers,
    required this.logo
  });
}